import yaml
import os
import json
import subprocess

# 1. Load Inventory
with open("inventory.yaml", 'r') as f:
    config = yaml.safe_load(f)

# Set defaults for network if not fully defined in inventory
net = config.get('network', {
    'gateway': '192.168.30.1',
    'dns': '1.1.1.1',
    'pxe_server': '192.168.30.2'
})

# Ensure directory structure exists [cite: 2, 5]
os.makedirs("talosconfig", exist_ok=True)
os.makedirs("dnsmasq", exist_ok=True)
os.makedirs("matchbox-data/profiles", exist_ok=True)
os.makedirs("matchbox-data/groups", exist_ok=True)

# 2. Generate Talos Secrets and Configs [cite: 11, 13]
if not os.path.exists("secrets.yaml"):
    print("🔐 Generating new secrets.yaml...")
    subprocess.run(["talosctl", "gen", "secrets", "-o", "secrets.yaml"])

print("⚙️ Generating Talos machine configurations...")
subprocess.run([
    "talosctl", "gen", "config", config['cluster_name'], 
    f"https://{config['endpoint']}:6443", 
    "--with-secrets", "secrets.yaml", 
    "--output-dir", "./talosconfig", 
    "--force"
])

# 3. Generate dnsmasq.conf (Master) 
dnsmasq_main = f"""# Main dnsmasq configuration - Generated by generate.py
interface=eth0
bind-interfaces

# DHCP Service
dhcp-range={net['gateway'].rsplit('.', 1)[0]}.50,{net['gateway'].rsplit('.', 1)[0]}.250,24h
dhcp-option=option:router,{net['gateway']}
dhcp-option=option:dns-server,{net['dns']}

# 1. Identify if the request is coming from iPXE
dhcp-userclass=set:ipxe,iPXE

# 2. Architecture Detection (for the first stage)
dhcp-match=set:efi64,option:client-arch,7
dhcp-match=set:efi64,option:client-arch,9
dhcp-match=set:bios,option:client-arch,0

# 3. The Chainload Logic
# If it is NOT iPXE, give them the bootloader to get iPXE started
dhcp-boot=tag:!ipxe,tag:bios,undionly.kpxe,,{net['pxe_server']}
dhcp-boot=tag:!ipxe,tag:efi64,ipxe.efi,,{net['pxe_server']}

# If IT IS already iPXE, tell it to fetch its config from Matchbox
# Note the :8081/boot.ipxe endpoint
dhcp-boot=tag:ipxe,http://{net['pxe_server']}:8081/boot.ipxe

# Matchbox PXE / TFTP Settings
enable-tftp
tftp-root=/var/lib/tftpboot
dhcp-option=66,{net['pxe_server']}

conf-file=/etc/dnsmasq.d/nodes.conf
"""

with open("dnsmasq/dnsmasq.conf", 'w') as f:
    f.write(dnsmasq_main)

# 4. Generate Nodes and Matchbox Logic 
dnsmasq_nodes = []

for node in config['nodes']:
    # Add to dnsmasq static leases [cite: 2]
    dnsmasq_nodes.append(f"dhcp-host={node['mac']},{node['ip']},{node['name']},infinite")

    # Define the Matchbox Profile (What to boot) [cite: 2]
    profile_name = f"profile-{node['type']}"
    profile_data = {
        "id": profile_name,
        "name": f"Talos {node['type']}",
        "boot": {
            "kernel": "/assets/vmlinuz",
            "initrd": ["/assets/initramfs.xz"],
            "args": [
                "initrd=initramfs.xz",
                f"talos.config=http://{net['pxe_server']}:8081/assets/{node['type']}.yaml",
                "talos.platform=metal",
                "slab_nomerge",
                "pti=on"
            ]
        }
    }
    
    with open(f"matchbox-data/profiles/{profile_name}.json", 'w') as f_prof:
        json.dump(profile_data, f_prof, indent=2)

    # Define the Matchbox Group (Who gets the profile) 
    group_data = {
        "id": node['name'],
        "name": f"Group for {node['name']}",
        "profile": profile_name,
        "selector": {
            "mac": node['mac'].lower()
        }
    }
    
    with open(f"matchbox-data/groups/{node['name']}.json", 'w') as f_group:
        json.dump(group_data, f_group, indent=2)

# Save nodes.conf 
with open("dnsmasq/nodes.conf", 'w') as f_nodes:
    f_nodes.write("\n".join(dnsmasq_nodes))

# 5. Link Talos configs to Matchbox 
# Matchbox looks in /var/lib/matchbox/configs/ by default
os.makedirs("matchbox-data/configs", exist_ok=True)
subprocess.run(f"cp talosconfig/controlplane.yaml matchbox-data/assets/controlplane.yaml", shell=True)
subprocess.run(f"cp talosconfig/worker.yaml matchbox-data/assets/worker.yaml", shell=True)

print("✅ Configuration Suite Complete.")
